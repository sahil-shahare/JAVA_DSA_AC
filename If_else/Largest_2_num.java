public class Largest_2_num{
    public static void main(String[] args){
        int num1 = 40;
        int num2 = 20;

        if(num1>num2){
            System.out.println("The Largest number is : " +num1);
        }
        else if(num2>num1){
            System.out.println("The Largest number is : " +num2);
        }
        else {
            System.out.println("Both numbers are equal.");
        }
    }
}